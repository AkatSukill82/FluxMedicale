export function I18nProvider({ children }) {
  const [locale, setLocale] = useState(() => {
    // Check for saved locale in localStorage, default to 'fr'
    const savedLocale = typeof window !== 'undefined' ? localStorage.getItem('fluxmed_locale') : 'fr';
    return savedLocale || 'fr';
  });

  useEffect(() => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('fluxmed_locale', locale);
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const t = (key, params = {}) => {
    const fallbackLocale = 'fr';
    let translation = translations[locale]?.[key] || translations[fallbackLocale]?.[key] || key;
    
    if (typeof translation !== 'string') {
        console.warn(`[i18n] Translation for key '${key}' in locale '${locale}' is not a string.`);
        return key;
    }
    
    // Replace parameters like {{name}}
    return translation.replace(/\{\{(\w+)\}\}/g, (match, paramKey) => {
      return params[paramKey] !== undefined ? params[paramKey] : match;
    });
  };

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
}